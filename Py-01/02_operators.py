### Operadores Aritmaéticos ### 

print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 3) #modulos
print(10 // 3)
print(2 ** 3) #exponente

print("Hola " + "Python " + "¿Qué tal?") #Concadena
print("Hola " + str(5)) 
print("Hola " + "5")
print("Hola " * 5)
print("Hola " * (2 ** 3))

my_float = 2.5 * 2
print("Hola " * int(my_float))

### Operadores Comparativos ### 

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(4 <= 4)
print(3 == 4)
print(3 != 4)

print("Hola" > "Python")
print("Hola" < "Python")
print("aaaa" >= "abaa") #ordenacion alfabetica por ASCII
print(len("aaaa") >= len("abaa")) #cuenta caracteres
print("Hola" <= "Python")
print("Hola" == "Hola")
print("Hola"!= "Python")

### Operadores Lógicos ### 

print(3 > 4 and "Hola" > "Python")
print(3 > 4 or "Hola" > "Python")
print(3 < 4 and "Hola" < "Python")
print(3 < 4 or "Hola" > "Python")
print(3 < 4 or ("Hola" > "Python" and 4 == 4))
print(not(3 > 4))
